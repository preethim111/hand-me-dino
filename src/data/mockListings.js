    'Feeding':    `${condition} feeding equipment, fully sanitized. All parts accounted for and functioning correctly.`,
    'Bath':       `${condition} bath item, sanitized and ready for use. Great for safe bath time routines.`,
    'Travel':     `${condition} travel gear in great condition. Folds/stores easily. Ideal for families on the go.`,
    'Other':      `${condition} baby product in great shape and ready for its next adventure with a new family.`,
